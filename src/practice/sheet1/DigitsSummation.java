package practice.sheet1;

import java.util.Scanner;
public class DigitsSummation {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        long a = sc.nextLong();
        long b = sc.nextLong();
        int lastDigitOfA = (int)(a % 10);
        int lastDigitOfB = (int)(b % 10);
        System.out.println(lastDigitOfA + lastDigitOfB);
        sc.close();
    }
}

